		/// Required method for Designer support - do not modify
		/// the contents of this method with the code editor.
		/// </summary>
		private void InitializeComponent()
		{    

		}
		#endregion

		#region LogOut 

		protected void lnkLogOut_Click(object sender, System.EventArgs e)
		{
			Session.Abandon();
			Response.Write("<script language=javascript>self.close();</script>");
			string nextpage = "LoginPage.aspx";
			Response.Write("<script language=javascript>");
			Response.Write("{");
			Response.Write(" var Backlen=history.length;");
			Response.Write(" history.go(-Backlen);");
			Response.Write(" window.location.href='" + nextpage + "'; ");
			Response.Write("}");
			Response.Write("</script>");  

			Response.Cache.SetExpires(DateTime.Parse(DateTime.  Now.ToString()));
			Response.Cache.SetCacheability(HttpCacheability.Private);
			Response.Cache.SetNoStore();
			Response.AppendHeader("Pragma", "no-cache");
			
		}


		#endregion

		#region CandidateSearch

		protected void lnkCandidateSearch_Click(object sender, System.EventArgs e)
		{
			Response.Redirect("CandidateSearchPage.aspx",false);
		}


		#endregion

		#region CompanyProfile

