		protected System.Web.UI.WebControls.LinkButton lbHireExit;
	
		#region PageLoad 

		protected void Page_Load(object sender, System.EventArgs e)
		{
			lnkLogOut.Attributes.Add("onclick","CloseWindow();");

			if(Session["CompanyId"] == null || Session["CompanyName"] == null)
			{
				Response.Redirect("./LoginPage.aspx",false);
			}
			else
			{
				lblCompanyName.Text=Session["CompanyName"].ToString();
			}
			
			if(!IsPostBack)
			{
				if(Session["IsUpdated"] != null)
				{
					if(Session["IsUpdated"].ToString() == "False")
					{
						this.Page.RegisterClientScriptBlock("Message", "<script language=javascript>alert('Please change your autogenerated password, for security reasons');</script>");
					}
				}
			}
		}


		#endregion

		#region Web Form Designer generated code
